        <div id="Ingrediëntenrecept">
          <ul>
            <h3>Ingrediënten:</h3>
            <li>80 g verse gember</li>
            <li> 1 teen knoflook</li>
            <li> 2 sjalotten</li>
            <li>800g koelverse broccoliroosjes</li>
            <li>800g kikkererwten in blik</li>
            <li> 4 el arachideolie</li>
            <li> 4 el appel azijn</li>
            <li> 520 g verse kabeljauwhaasjes</li>
            <li> 10 g verse platte peterselie</li>
          </ul>
        </div>
        <div class="Bereidenbroccoli">
          <ol>
            <h2>Bereiden:</h2>
            <li> Verwarm de oven voor op 200 °C.<br></li>
            <li> Schil en rasp de gember. Snijd de knoflook fijn en snipper de sjalot. Halveer de grote broccoliroosjes.
              Doe de kikkererwten in een vergiet en spoel af onder koud stromend water. Laat goed uitlekken. <br> </li>
            <li> Verdeel de broccoli en kikkererwten over een met bakpapier beklede bakplaat, besprenkel met de helft
              van de olie en bestrooi met peper en zout. Rooster ca. 20 min. in de oven. Schep halverwege om. <br> </li>
            <li> Maak ondertussen de dressing. Meng de rest van de olie met de gember, knoflook, sjalot en appelazijn.
              Breng op smaak met peper. <br> </li>
            <li> Leg de kabeljauw in de ingevette ovenschaal en besmeer met 2 el dressing. Schuif na 5 min. onder de
              broccoli en kikkererwten in de oven en gaar ca. 15 min. in de oven. <br> </li>
            <li> Snijd ondertussen de peterselie grof. Neem de broccoli en vis uit de oven. Schep de warme broccoli en
              kikkererwten om met de peterselie en de rest van de dressing. Serveer met de vis. <br> </li>
          </ol>
        </div>
